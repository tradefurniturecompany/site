namespace Customweb\RealexCw\Block\Adminhtml\Transaction;

class AbstractTransaction extends \Magento\Backend\Block\Widget
{
    /**
     * Core registry
     *
     * @var \Magento\Framework\Registry
     */
    protected $_coreRegistry = null;

    /**
     * Admin helper
     *
     * @var \Magento\Sales\Helper\Admin
     */
    protected $_adminHelper;

    /**
     * @param \Magento\Backend\Block\Template\Context $context
     * @param \Magento\Framework\Registry $registry
     * @param \Magento\Sales\Helper\Admin $adminHelper
     * @param array $data
     */
    public function __construct(
        \Magento\Backend\Block\Template\Context $context,
        \Magento\Framework\Registry $registry,
        \Magento\Sales\Helper\Admin $adminHelper,
        array $data = []
    ) {
        $this->_adminHelper = $adminHelper;
        $this->_coreRegistry = $registry;
        parent::__construct($context, $data);
    }

    /**
     * Retrieve available transaction
     *
     * @return \Customweb\RealexCw\Model\Authorization\Transaction
     * @throws \Magento\Framework\Exception\LocalizedException
     */
    public function getTransaction()
    {
        if ($this->hasTransaction()) {
            return $this->getData('transaction');
        }
        if ($this->_coreRegistry->registry('realexcw_transaction')) {
            return $this->_coreRegistry->registry('realexcw_transaction');
        }
        throw new \Magento\Framework\Exception\LocalizedException(__('We cannot get the transaction instance.'));
    }
}
